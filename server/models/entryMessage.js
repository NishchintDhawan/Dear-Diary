import mongoose from 'mongoose';

const entrySchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var EntryMessage = mongoose.model('EntryMessage', entrySchema);

export default EntryMessage;